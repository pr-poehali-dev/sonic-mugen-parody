export interface SuperForm {
  name: string;
  emoji: string;
  colorHex: string;
  bgGlow: string;
  powerBoost: number;
  speedBoost: number;
  defenseBoost: number;
  aura: string;
  transformText: string;
  superSpecial: string;
}

export interface Character {
  id: string;
  name: string;
  emoji: string;
  color: string;
  colorHex: string;
  bgGradient: string;
  // Stats 1-10 — РЕАЛЬНО влияют на бой
  speed: number;       // Скорость ИИ-ответа и скорость хода в MUGEN
  power: number;       // Базовый урон
  defense: number;     // Снижение входящего урона
  agility: number;     // Шанс уклонения ИИ
  critChance: number;  // Шанс крита (в %)
  maxHP: number;       // Базовые HP (реально разные)
  description: string;
  style: string;
  origin: string;      // Вселенная персонажа
  combos: string[];
  special: string;
  specialKey: string;
  rarity: Rarity;
  price: number;       // Цена в кольцах
  priceGems: number;   // Цена в кристаллах (0 = нельзя купить за кристаллы)
  unlocked?: boolean;  // По умолчанию false (кроме стартовых)
  superForm?: SuperForm;
}

